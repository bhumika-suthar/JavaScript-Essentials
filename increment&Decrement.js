var num = 0
for(var i = 0 ; i <= 10; i++){
    console.log(i);
}
console.log("For increment ");
console.log();
//postfix = print first then increment
console.log(num++);
//prefix = increment first then print
console.log(++num);

console.log()
console.log("For decrement ")
//postfix = print first then decrement
console.log(num--);
//prefix = decrement first then print
console.log(--num);

